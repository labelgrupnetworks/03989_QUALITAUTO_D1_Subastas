        <h1 class="ff-highlight fs-24-40 text-center">COOKIE POLICY</h1>

        <p><strong>1. What are cookies?</strong></p>
        <p>The website owned by ANSORENA, S.A. (hereinafter “the website” or “the site”) uses cookies and/or similar
            technologies that store and retrieve information when you browse. In general, these technologies may serve
            various purposes, such as recognizing you as a user, obtaining information about your browsing habits, or
            customizing how content is displayed.</p>
        <p>The user may configure their browser to notify and reject the installation of cookies sent by the website
            without affecting the ability to access the content. However, the quality of the website's performance may
            decrease.</p>
        <p>The specific uses of these technologies are described below.</p>

        <p><strong>2. What types of cookies are there?</strong></p>
        <p>Cookies, depending on their duration, can be classified as:</p>

        <table class="table table-bordered table-striped">
            <tbody>
                <tr>
                    <td><strong>Session cookies</strong></td>
                    <td>These are designed to collect and store data while the user accesses a website. They are
                        typically used to store information that is only relevant for providing the requested service.
                        They expire when the user closes the browser.</td>
                </tr>
                <tr>
                    <td><strong>Persistent cookies</strong></td>
                    <td>These remain stored on the device and can be accessed and processed for a defined period
                        depending on the purpose for which they are used (for example, to keep the user logged in) or
                        until they are manually deleted.</td>
                </tr>
            </tbody>
        </table>

        <p>Depending on the ownership, they can be classified as:</p>

        <table class="table table-bordered table-striped">
            <tbody>
                <tr>
                    <td><strong>First-party cookies</strong></td>
                    <td>These are sent to the user's device from a system or domain managed by the website editor from
                        which the service requested by the user is provided.</td>
                </tr>
                <tr>
                    <td><strong>Third-party cookies</strong></td>
                    <td>These are sent to the user's device from a system or domain not managed by the website editor,
                        but by another entity that processes the data obtained through the cookies.</td>
                </tr>
            </tbody>
        </table>

        <p>Additionally, based on their purpose, cookies can be classified as follows:</p>

        <table class="table table-bordered table-striped">
            <tbody>
                <tr>
                    <td><strong>Technical cookies</strong></td>
                    <td>These allow the user to navigate through a website, platform, or application and use the
                        different options or services available, including those used by the editor to manage and
                        operate the website, enable its functions and services, such as identifying the session or
                        controlling traffic and data communication.</td>
                </tr>
                <tr>
                    <td><strong>Preference or customization cookies</strong></td>
                    <td>This type of cookie remembers your preferences for the tools found in the services, so you don't
                        have to configure them every time you visit. Examples include:<br>
                        - Volume settings of video or audio players.<br>
                        - Video transmission speeds compatible with your browser.
                    </td>
                </tr>
                <tr>
                    <td><strong>Geolocation cookies</strong></td>
                    <td>These are used to determine which country you are in when requesting a service. This cookie is
                        completely anonymous and is only used to help tailor content to your location.</td>
                </tr>
                <tr>
                    <td><strong>Login cookies</strong></td>
                    <td>These are generated once the user has registered or subsequently logged in and are used to
                        identify the user in the services with the following purposes:<br>
                        - Keep the user logged in so that if they close a service, the browser, or the computer and
                        return later, they remain identified, facilitating navigation without having to log in again.
                        This functionality can be disabled if the user clicks "log out", which deletes the cookie and
                        requires a new login on the next visit.<br>
                        - Verify if the user is authorized to access certain services, such as participating in a
                        contest.
                    </td>
                </tr>
                <tr>
                    <td><strong>Analytics or measurement cookies</strong></td>
                    <td>Each time a user visits a service, an external provider's tool generates an analytical cookie on
                        the user's device. This cookie, generated only during the visit, will help identify the user
                        anonymously in future visits. Main goals:<br>
                        - Allow anonymous identification of browsing users through the cookie (identifies browsers and
                        devices, not people), enabling approximate visitor counts and trend analysis.<br>
                        - Identify the most visited and attractive content for users.<br>
                        - Know if the user is new or returning.<br>
                        <strong>Important:</strong> Unless the user registers for a service, the cookie will never be
                        linked to personal data. These cookies are only used for statistical purposes to optimize the
                        user experience.
                    </td>
                </tr>
                <tr>
                    <td><strong>Behavioral advertising cookies</strong></td>
                    <td>This type of cookie helps gather more information about the ads shown to each anonymous user on
                        the website. It stores the duration or frequency of ad viewing, interactions, or user browsing
                        patterns to build a profile of advertising interest. This allows for advertising tailored to the
                        user's interests.</td>
                </tr>
                <tr>
                    <td><strong>Third-party advertising cookies</strong></td>
                    <td>In addition to ads managed by the website, third-party advertisers (“AdServers”) may serve ads.
                        These third parties can store cookies sent from the website services through users’ browsers and
                        access the data stored in them.</td>
                </tr>
            </tbody>
        </table>

