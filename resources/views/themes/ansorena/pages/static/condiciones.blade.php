    <h1 class="ff-highlight fs-24-40 text-center">CONDICIONES</h1>

    <h2 class="ff-highlight">I. DEFINICI&Oacute;N</h2>
    <p>Ansorena S.A. desarrolla su actividad de subastas en un marco de contrataci&oacute;n, en adelante la Sala,
        organizado
        mediante la venta en remate, previa subasta voluntaria, de objetos art&iacute;sticos depositados en Ansorena
        para
        tal fin. En consecuencia, Ansorena act&uacute;a a trav&eacute;s de su actividad en la Sala como mediadora
        entre
        el
        propietario vendedor de cada lote y los posibles compradores.</p>
    <h2 class="ff-highlight">II. CONDICIONES GENERALES</h2>
    <h3 class="ff-highlight">1. INFORMACI&Oacute;N GENERAL</h3>
    <p>Las informaciones generales que figuran en nuestros cat&aacute;logos y anuncios est&aacute;n realizadas y
        supervisadas tras su estudio por personal experto de Ansorena licenciados en arte. En casos particulares de
        joyas,
        relojes, iconos, plata, etc., se realiza su valoraci&oacute;n por los m&aacute;s cualificados expertos en
        cada
        materia.</p>
    <h3 class="ff-highlight">2. EXPOSICI&Oacute;N</h3>
    <p>Los objetos u obras de arte que han de subastarse, ser&aacute;n expuestos, al menos con diez d&iacute;as de
        antelaci&oacute;n, en los locales de la Sala, a fin de permitir un perfecto examen y conocimiento de los
        mismos
        a
        los posibles compradores.</p>
    <h3 class="ff-highlight">3. ESTADO DE LOS LOTES</h3>
    <p>Los lotes su subastar&aacute;n en el estado en que se encuentren, no aceptando la Sala ninguna
        reclamaci&oacute;n
        sobre las restauraciones, desperfectos, roturas, etc., aunque no se hayan hecho constar en el
        cat&aacute;logo.
        La
        exposici&oacute;n de los lotes tiene por finalidad permitir un perfecto examen y estudios de los mismos por
        las
        personas interesadas.</p>
    <h3 class="ff-highlight">4. CATALOGACI&Oacute;N DE LOS LOTES</h3>
    <p>La Sala se reserva el derecho de catalogar los lotes, cambiarlos o subdividirlos en varios, de conformidad a
        su
        prudente y experto criterio y podr&aacute; asimismo retirarlos por causa justificada.</p>
    <h3 class="ff-highlight">5. ADMISI&Oacute;N DE OBJETOS</h3>
    <p>Un equipo de expertos, previo estudio completo, establecer&aacute; de acuerdo con el vendedor un precio
        inicial
        id&oacute;neo de salida.</p>
    <h3 class="ff-highlight">6. USOS Y NORMAS</h3>
    <p>En su car&aacute;cter de mediadora, la Sala realizar&aacute; la subasta de los objetos u obras de arte que se
        le
        encomienden conforme a las normas en cada momento aplicables y vigentes, as&iacute; como a los usos
        comerciales
        por
        los que tradicionalmente vienen rigi&eacute;ndose este tipo de operaciones. Ansorena actuar&aacute;
        s&oacute;lo
        como
        intermediaria, no siendo responsable de la exactitud de las afirmaciones acerca del autor, origen, fecha,
        antig&uuml;edad, procedencia y estado del lote, aunque se da fe de que las mismas son el resultado del
        estudio e
        investigaci&oacute;n realizada por sus expertos a su mejor y leal entender.</p>
    <p>No obstante, la venta de un lote podr&aacute; ser rescindida y su precio devuelto al comprador, si
        &eacute;ste
        notificara por escrito a Ansorena, antes de los quince d&iacute;as naturales siguientes a la
        celebraci&oacute;n
        de
        la subasta, que en su opini&oacute;n el lote es una falsificaci&oacute;n deliberada, debiendo demostrarlo de
        una
        manera fehaciente.</p>
    <h3 class="ff-highlight">7. CIFRA DE SALIDA</h3>
    <p>La cifra de salida de cada objeto u obra en la subasta se determinar&aacute; en el cat&aacute;logo.
        &Eacute;sta
        constituir&aacute; el precio m&iacute;nimo de venta, salvo en los casos excepcionales en los que se pueda
        pactar
        una
        reserva con el vendedor.</p>
    <h3 class="ff-highlight">8. INCREMENTO M&Iacute;NIMO</h3>
    <p>El Director de la Subasta tiene derecho a rechazar toda puja que no sobrepase la precedente en la
        cuant&iacute;a
        en
        cada caso aplicable.</p>
    <h3 class="ff-highlight">9. ADJUDICACI&Oacute;N AL MEJOR POSTOR. DESACUERDOS</h3>
    <p>Los lotes se adjudicar&aacute;n al mejor postor. En caso de desacuerdo entre dos o m&aacute;s licitadores, la
        Sala
        tendr&aacute; la facultad de proceder a una nueva subasta en la misma sesi&oacute;n, o bien en la inmediata
        siguiente.</p>
    <h3 class="ff-highlight">10. SERVICIO DE INFORMACI&Oacute;N Y ASESORAMIENTO</h3>
    <p>La Sala pone a disposici&oacute;n del p&uacute;blico, durante los d&iacute;as y horas de exposici&oacute;n,
        un
        servicio de asesoramiento constituido por nuestros expertos, que informar&aacute;n sobre el estado de
        conservaci&oacute;n, autor a qui&eacute;n se atribuye, precios de mercado, o cualquier otro dato relacionado
        con
        los
        lotes incluidos en el cat&aacute;logo para la subasta.</p>
    <p>Los servicios de asesoramiento expresados se presentar&aacute;n conforme al razonable y prudente juicio de la
        Sala,
        pero sin asumir responsabilidad alguna en cuanto al contenido de la informaci&oacute;n.</p>
    <h3 class="ff-highlight">11. VALORACIONES</h3>
    <p>La Sala dispone de un departamento de valoraci&oacute;n de pintura, joyas y objetos de arte que realiza
        tasaciones
        por escrito, para seguros, testamentarias, reparto de bienes, adjudicaciones en pago de deudas y otros,
        realizando
        estas tasaciones con arreglo a las siguientes tarifas sobre los importes totales de valoraci&oacute;n:</p>
    <ul>
        <li>1,5% sobre valoraci&oacute;n hasta 30.000&euro;</li>
        <li>1% sobre valoraci&oacute;n superior a 30.000&euro;</li>
    </ul>
    <p>*Para valoraciones inferiores a 10.000&euro;, el importe m&iacute;nimo de la tasaci&oacute;n por escrito
        ser&aacute;
        de 300&euro;.</p>
    <h3 class="ff-highlight">12. SUMISI&Oacute;N Y LEY APLICABLE</h3>
    <p>En caso de que surgieran discrepancias entre las partes por raz&oacute;n de las operaciones de subasta, el
        vendedor,
        el adjudicatario o comprador y la propia Sala, con renuncia a cualquier otro fuero que pudiera corresponder,
        se
        someter&aacute;n a la Ley Espa&ntilde;ola y jurisdicci&oacute;n de los Juzgados y Tribunales de Madrid.</p>
    <h3 class="ff-highlight">13. TASACIONES DE OTROS EXPERTOS</h3>
    <p>En el caso en el que el vendedor o comprador desee aportar certificaciones de expertos ajenos a la Sala
        respecto
        a un
        lote, los gastos que se devenguen ser&aacute;n de su cuenta, no admitiendo la Sala responsabilidad por
        errores
        en la
        calificaci&oacute;n o expertizaci&oacute;n.</p>
