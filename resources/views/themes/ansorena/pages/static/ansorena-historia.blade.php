            <div class="col-md-5">
                <h2 class="ff-highlight fs-24-32 mb-5">
                    Ansorena nace en 1845, cuando Celestino Ansorena abre en Madrid su taller de joyería. Muy pronto se
                    consagra como uno de los joyeros más importantes de le época y en 1860 es nombrado “Joyería y
                    diamantista de la Real Casa”.
                </h2>
            </div>
            <div class="col-md-4 offset-md-1 text-justify">
                <p>La relación con la Casa Real se afianza cuando Alfonso XIII regala, por motivo de su boda en 1906, a
                    la Reina Victoria Eugenia, un collar de diamantes en forma de chatones y una tiara de diamantes con
                    flores de lis dinásticas, que hemos visto en numerosas actos oficiales a la Reina Sofia y a la Reina
                    Letizia.</p>

                <p>En la década de los años 70, Alfonso Mato transformará el negocio de la familia de su esposa, Paloma
                    Ansorena, en una empresa de actividades complementarias como son la joyería y las subastas de arte,
                    campo en el que será una de las pioneras en España.</p>

                <p>Hoy, casi cincuenta años después de su primera subasta, ambas actividades siguen vigentes, siendo una
                    de las casas de subastas de arte más y prestigiosas del país.</p>
                <p>Ansorena dispone de un servicio de valoración de pintura, joyas y objetos de arte, realizados por un
                    equipo de expertos, actividad con la que se han conseguido extraordinarios resultados.</p>

                <p>El equipo de especialistas con gran experiencia, realizan tasaciones para seguros, repartos de bienes
                    y testamentarias.</p>

                <p>Ansorena también gestiona Ventas Privadas, una manera muy eficaz tanto para la venta como para la
                    compra de obras relevantes, durante los últimos años se ha realizado de forma exitosa ventas a
                    Museos, Instituciones o colecciones privadas.</p>
