                <p>Desde 1986 Ansorena cuenta con una Galeria de Arte Contemporáneo, donde se realizan exposiciones
                    mensuales tanto de pintores consagrados como de jóvenes promesas. Especializada en pintura relista y
                    figurativa, colabora con organismo oficiales en la celebración y montajes de exposiciones que
                    contribuyen al mayor conocimiento de la pintura realista contemporánea. Participa habitualmente en
                    ferias nacionales e internacionales como Miami, Chicago o Londres.</p>

                <p>Con una importante exposición en el Museo Nacional de Artes Decorativas de Madrid se celebraron los
                    150 años de la firma donde se pudieron contemplar joyas realizadas por los talleres de Ansorena
                    durante su siglo y medio de existencia.
                    En 2010, Ansorena, en su exposición “Diademas desde 1845” reunió las tiaras icónicas de su joyería
                    desde 1845 incluida la tiara “Flor de Lis” de Casa Real.
