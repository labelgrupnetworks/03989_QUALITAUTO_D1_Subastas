            navegador o su gestor de correo electrónico, así como instalar complementos gratuitos para evitar que se
            descarguen los Web Bugs al abrir un email.</p>
        <p>La Web ofrece orientación al Usuario sobre los pasos para acceder al menú de configuración de las cookies y,
