        <h1 class="ff-highlight fs-24-40 text-center">POLÍTICA DE COOKIES</h1>

        <p><strong>1. ¿Qué son las cookies?</strong></p>
        <p>La página web cuya propiedad y titularidad es de ANSORENA, S.A. (en adelante “la web” o el “sitio web”)
            utiliza cookies y/o tecnologías similares que almacenan y recuperan información cuando navegas. En general,
            estas tecnologías pueden servir para finalidades muy diversas, como, por ejemplo, reconocerte como usuario,
            obtener información sobre tus hábitos de navegación, o personalizar la forma en que se muestra el contenido.
        </p>
        <p>El usuario podrá configurar su navegador para que notifique y rechace la instalación las cookies enviadas por
            la web, sin que ello perjudique la posibilidad de acceder a los contenidos de dicha web, pero la calidad de
            funcionamiento de la página web puede disminuir.</p>
        <p>Los usos concretos que hacemos de estas tecnologías se describen a continuación.</p>

        <p><strong>2. ¿Qué tipos de cookies existen?</strong></p>
        <p>Las Cookies, en función de su Permanencia, pueden dividirse en:</p>

        <table class="table table-bordered table-striped">
            <tbody>
                <tr>
                    <td><strong>Cookies de sesión</strong></td>
                    <td>Son aquellas diseñadas para recabar y almacenar datos mientras el usuario accede a una página
                        web. Se suelen emplear para almacenar información que solo interesa conservar para la prestación
                        del servicio solicitado. Expiran cuando el usuario cierra el navegador.</td>
                </tr>
                <tr>
                    <td><strong>Cookies persistentes</strong></td>
                    <td>Son aquellas en las que los datos siguen almacenados en el terminal y pueden ser accedidos y
                        tratados durante un periodo definido en función de cuando se cumpla el objetivo para el que
                        sirven (por ejemplo, para que el usuario se mantenga identificado en los Servicios) o bien
                        cuando se borran manualmente.</td>
                </tr>
            </tbody>
        </table>

        <p>En función de su Propietario, pueden dividirse en:</p>

        <table class="table table-bordered table-striped">
            <tbody>
                <tr>
                    <td><strong>Cookies propias</strong></td>
                    <td>Son aquéllas que se envían al equipo terminal del usuario desde un equipo o dominio gestionado
                        por el propio editor y desde el que se presta el servicio solicitado por el usuario.</td>
                </tr>
                <tr>
                    <td><strong>Cookies de terceros</strong></td>
                    <td>Son aquéllas que se envían al equipo terminal del usuario desde un equipo o dominio que no es
                        gestionado por el editor, sino por otra entidad que trata los datos obtenidos través de las
                        cookies.</td>
                </tr>
            </tbody>
        </table>

        <p>Adicionalmente, en función de su finalidad, las Cookies pueden clasificarse de la siguiente forma:</p>

        <table class="table table-bordered table-striped">
            <tbody>
                <tr>
                    <td><strong>Cookies técnicas</strong></td>
                    <td>Son aquellas que permiten al usuario la navegación a través de una página web, plataforma o
                        aplicación y la utilización de las diferentes opciones o servicios que en ella existan,
                        incluyendo aquellas que el editor utiliza para permitir la gestión y operativa de la página web
                        y habilitar sus funciones y servicios como, por ejemplo, identificar la sesión o controlar el
                        tráfico y la comunicación de datos.</td>
                </tr>
                <tr>
                    <td><strong>Cookies de preferencias o personalización</strong></td>
                    <td>Este tipo de Cookie recuerda sus preferencias para las herramientas que se encuentran en los
                        servicios, por lo que no tiene que volver a configurar el servicio cada vez que usted visita. A
                        modo de ejemplo, en esta tipología se incluyen:<br>
                        - Ajustes de volumen de reproductores de vídeo o sonido.<br>
                        - Las velocidades de transmisión de vídeo que sean compatibles con su navegador.
                    </td>
                </tr>
                <tr>
                    <td><strong>Cookies de Geo-localización</strong></td>
                    <td>Estas Cookies son utilizadas para averiguar en qué país se encuentra cuando se solicita un
                        servicio. Esta Cookie es totalmente anónima, y sólo se utiliza para ayudar a orientar el
                        contenido a su ubicación.</td>
                </tr>
                <tr>
                    <td><strong>Cookies de registro</strong></td>
                    <td>Las Cookies de registro se generan una vez que el usuario se ha registrado o posteriormente ha
                        abierto su sesión, y se utilizan para identificarle en los servicios con los siguientes
                        objetivos:<br>
                        - Mantener al usuario identificado de forma que, si cierra un servicio, el navegador o el
                        ordenador y en otro momento u otro día vuelve a entrar en dicho servicio, seguirá identificado,
                        facilitando así su navegación sin tener que volver a identificarse. Esta funcionalidad se puede
                        suprimir si el usuario pulsa la funcionalidad “cerrar sesión”, de forma que esta Cookie se
                        elimina y la próxima vez que entre en el servicio el usuario tendrá que iniciar sesión para
                        estar identificado.<br>
                        - Comprobar si el usuario está autorizado para acceder a ciertos servicios, por ejemplo, para
                        participar en un concurso.
                    </td>
                </tr>
                <tr>
                    <td><strong>Cookies de análisis o medición</strong></td>
                    <td>Cada vez que un Usuario visita un servicio, una herramienta de un proveedor externo genera una
                        Cookie analítica en el ordenador del usuario. Esta Cookie que sólo se genera en la visita,
                        servirá en próximas visitas a los Servicios de la Web para identificar de forma anónima al
                        visitante. Los objetivos principales que se persiguen son:<br>
                        - Permitir la identificación anónima de los usuarios navegantes a través de la “Cookie”
                        (identifica navegadores y dispositivos, no personas) y por lo tanto la contabilización
                        aproximada del número de visitantes y su tendencia en el tiempo.<br>
                        - Identificar de forma anónima los contenidos más visitados y por lo tanto más atractivos para
                        los usuarios.<br>
                        - Saber si el usuario que está accediendo es nuevo o repite visita.<br>
                        <strong>Importante:</strong> Salvo que el usuario decida registrarse en un servicio de la Web,
                        la “Cookie” nunca irá asociada a ningún dato de carácter personal que pueda identificarle.
                        Dichas Cookies sólo serán utilizadas con propósitos estadísticos que ayuden a la optimización de
                        la experiencia de los Usuarios en el sitio.
                    </td>
                </tr>
                <tr>
                    <td><strong>Cookies de publicidad comportamental</strong></td>
                    <td>Este tipo de “Cookies” permite ampliar la información de los anuncios mostrados a cada usuario
                        anónimo en los Servicios de la Web. Entre otros, se almacena la duración o frecuencia de
                        visualización de posiciones publicitarias, la interacción con las mismas, o los patrones de
                        navegación y/o compartimientos del usuario ya que ayudan a conformar un perfil de interés
                        publicitario. De este modo, permiten ofrecer publicidad afín a los intereses del usuario.</td>
                </tr>
                <tr>
                    <td><strong>Cookies publicitarias de terceros</strong></td>
                    <td>Además de la publicidad gestionada por la Web en sus Servicios, la Web ofrece a sus anunciantes
                        la opción de servir anuncios a través de terceros (“AdServers”). De este modo, estos terceros
                        pueden almacenar Cookies enviadas desde los Servicios de la Web procedentes de los navegadores
                        de los Usuarios, así como acceder a los datos que en ellas se guardan.</td>
                </tr>
            </tbody>
        </table>

