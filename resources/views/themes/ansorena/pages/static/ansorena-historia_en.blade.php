                <h2 class="ff-highlight fs-24-32 mb-5">
                    Ansorena begins its story in 1845, when Celestino Ansorena decides to open his jeweller’s workshop
                    in Madrid. Very soon, he is consecrated as one of the most important jewellers of the age and in
                    1860 is named “Jeweller and diamond merchant to the Royal Household”.
                </h2>
            </div>
            <div class="col-md-4 offset-md-1 text-justify">
                <p>The relationship with the Royal Family was strengthened when Alfonso XIII gifted Queen Victoria
                    Eugenia a diamond collar in the shape of chatons and a diamond tiara with dynastic fleur-de-lis on
                    the occasion of their wedding in 1906. We have seen these pieces at numerous official events worn by
                    Queen Sofia and Queen Letizia.</p>
                <p>In the 1970s, Alfonso Mato transformed his wife Paloma Ansorena's family business into a company with
                    complementary activities such as jewelry and art auctions, making it one of the pioneers in Spain.
                </p>
                <p>Today, almost fifty years after its first auction, both activities are still in operation, and it
                    remains one of the most prestigious art auction houses in the country. Ansorena provides a valuation
                    service for paintings, jewelry, and art objects, carried out by a team of experts who have achieved
                    extraordinary results.</p>
                <p>The team of specialists with extensive experience performs appraisals for insurance, property
                    distribution, and wills.</p>
                <p>Ansorena also manages Private Sales, a highly effective way to both buy and sell significant works.
                    Over the last few years, successful sales have been made to museums, institutions, and private
                    collections.</p>
