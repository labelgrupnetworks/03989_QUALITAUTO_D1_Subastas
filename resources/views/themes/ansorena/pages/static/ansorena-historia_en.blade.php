                <p>Since 1986, Ansorena has had a Contemporary Art Gallery, where monthly exhibitions are held featuring
                    both established painters and young talents. Specializing in realistic and figurative painting, it
                    collaborates with official bodies in organizing and setting up exhibitions that contribute to a
                    better understanding of contemporary realistic painting. It regularly participates in national and
                    international fairs such as Miami, Chicago, and London.</p>
                <p>The 150th anniversary of the firm was celebrated with a major exhibition at the National Museum of
                    Decorative Arts in Madrid, where jewelry made by Ansorena's workshops during its century and a half
                    of existence could be admired. In 2010, Ansorena's exhibition "Tiaras since 1845" brought together
                    the iconic tiaras from its jewelry collection since 1845, including the "Flor de Lis" tiara from the
